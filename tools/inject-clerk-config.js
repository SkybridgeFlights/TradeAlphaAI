'use strict';

// Phase 220 activation — build-time Clerk config injection.
//
// Reads CLERK_PUBLISHABLE_KEY + CLERK_INSTANCE_URL from process.env (set
// in Vercel project settings) and writes js/clerk-config.js with the
// PUBLIC publishable key. The publishable key is safe to expose; the
// SECRET key is NEVER read here.
//
// When env vars are absent (local dev, no provisioning), writes a config
// with mode='unconfigured' so the client bootstrap shows a friendly
// fallback instead of breaking.
//
// Runs as part of `vercel build` via vercel.json buildCommand.

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'js', 'clerk-config.js');

const pub = process.env.CLERK_PUBLISHABLE_KEY || '';
const instanceUrl = process.env.CLERK_INSTANCE_URL || '';
const stamp = new Date().toISOString();

// Anti-leak: never write the SECRET key. Hard-fail if someone ever tries
// to pipe it through this entrypoint.
if (process.env.CLERK_SECRET_KEY && process.argv.includes('--include-secret')) {
  console.error('[clerk-config] FATAL: refusing to write CLERK_SECRET_KEY into a public file.');
  process.exit(1);
}

// Light validation of the publishable key shape (pk_test_/pk_live_ + base64ish body).
const valid = /^pk_(test|live)_[A-Za-z0-9_-]{20,}$/.test(pub);
const mode = valid ? 'hosted' : 'unconfigured';

const banner = `// Generated by tools/inject-clerk-config.js at ${stamp}.\n// PUBLIC publishable key only; SECRET key is NEVER written here.\n`;

const body = `window.__CLERK_CONFIG__ = ${JSON.stringify({
  mode,
  publishable_key: valid ? pub : null,
  instance_url: instanceUrl || null,
  generated_at: stamp,
})};\n`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, banner + body, 'utf8');

console.log(`[clerk-config] mode=${mode} key_present=${valid} instance=${instanceUrl ? 'set' : 'unset'} → ${path.relative(ROOT, OUT)}`);
