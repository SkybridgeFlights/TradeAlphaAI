'use strict';

// Phase 61: Responsive Layout Static Validator
// Scans HTML and CSS files for common mobile-layout risks.
//
// Checks:
//  1. HTML pages missing viewport meta
//  2. HTML pages missing /css/responsive.css link
//  3. Tables not inside a scroll wrapper
//  4. Fixed widths > 768px in inline styles
//  5. Images without max-width handling
//  6. pre/code blocks without overflow
//  7. Arabic pages without dir=rtl
//  8. Generated article pages missing responsive CSS
//  9. min-width values > 100vw in CSS
// 10. Unbreakable long URLs/strings in content
//
// Usage: node tools/check-responsive-layout.js
// Exit 0 on pass, exit 1 on failure.

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');

// Directories to scan for HTML files
const HTML_DIRS = [
  'insights', 'ar/insights', 'en/insights',
  'market-outlook', 'ar/market-outlook', 'en/market-outlook',
  'economic-calendar', 'ar/economic-calendar',
  'market-outlook/daily', 'market-outlook/weekly',
  '.'   // root-level HTML (index.html, stocks.html, etc.)
];

// Directories that should NOT be scanned
const SKIP_DIRS = new Set(['node_modules', 'drafts', '.git', 'tools', 'data', 'js', 'fonts', 'Image', 'icons']);

// Patterns that indicate a scrollable table wrapper
const TABLE_SCROLL_CLASSES = [
  'table-scroll', 'editorial-table-wrap', 'calendar-table-wrap', '.table-wrap',
  'ranking-table-wrap', 'table-container', 'overflow-x'
];

const failures = [];
const warnings = [];
let pagesChecked = 0;
let tablesChecked = 0;

function collectHtmlFiles() {
  const files = [];
  for (const dir of HTML_DIRS) {
    const absDir = path.join(ROOT, dir);
    if (!fs.existsSync(absDir)) continue;
    const entries = fs.readdirSync(absDir, { withFileTypes: true });
    for (const entry of entries) {
      if (!entry.isFile() || !entry.name.endsWith('.html')) continue;
      files.push(path.join(absDir, entry.name));
    }
  }
  return [...new Set(files)]; // deduplicate root-level files
}

function rel(file) { return path.relative(ROOT, file).replaceAll('\\', '/'); }

// ── Per-page checks ───────────────────────────────────────────────────────────

function checkPage(file) {
  const content = fs.readFileSync(file, 'utf8');
  const relPath = rel(file);
  pagesChecked++;

  // 1. Viewport meta
  if (!/<meta[^>]+name=["']viewport["'][^>]*>/i.test(content)) {
    failures.push(`${relPath}: missing <meta name="viewport">`);
  }

  // 2. Responsive CSS link
  if (!content.includes('/css/responsive.css') && !content.includes('css/responsive.css')) {
    // Only warn (not fail) for pages not generated by our pipeline
    const isGenerated = /insights\/|market-outlook\/|economic-calendar\/|ar\//.test(relPath);
    if (isGenerated) {
      warnings.push(`${relPath}: missing /css/responsive.css link`);
    }
    // Root-level pages just get a warning too — they have different link paths
  }

  // Strip <script> and <style> tag content so their string literals aren't scanned as HTML
  const contentNoScript = content
    .replace(/<script\b[^>]*>[\s\S]*?<\/script>/gi, (m) => ' '.repeat(m.length))
    .replace(/<style\b[^>]*>[\s\S]*?<\/style>/gi,  (m) => ' '.repeat(m.length));

  // 3. Tables without scroll wrapper (skip script/style text)
  const tableMatches = [...contentNoScript.matchAll(/<table[\s>]/gi)];
  for (const match of tableMatches) {
    tablesChecked++;
    // Get 500 chars of context before the table
    const before = contentNoScript.slice(Math.max(0, match.index - 500), match.index);
    const hasWrapper = TABLE_SCROLL_CLASSES.some((cls) => before.includes(cls));
    if (!hasWrapper) {
      // Check if the table itself has overflow styling
      const tableTag = contentNoScript.slice(match.index, match.index + 200);
      if (!/<table[^>]*overflow[^>]*>/.test(tableTag)) {
        warnings.push(`${relPath}: table at offset ${match.index} may not be in a scroll wrapper`);
      }
    }
  }

  // 4. Inline fixed widths > 768px — exclude max-width and min-width (they are responsive)
  //    Negative lookbehind ensures "width:" is not preceded by "max-" or "min-"
  const fixedWidths = [...content.matchAll(/style="[^"]*(?<![a-z-])width\s*:\s*(\d+)px[^"]*"/gi)];
  for (const m of fixedWidths) {
    if (parseInt(m[1], 10) > 768) {
      warnings.push(`${relPath}: inline width:${m[1]}px may overflow mobile (${m[0].slice(0, 60)})`);
    }
  }

  // 5. Images without max-width (only check img tags that have explicit width but no max-width)
  const imgWithWidth = [...content.matchAll(/<img[^>]+width=["'](\d+)["'][^>]*>/gi)];
  for (const m of imgWithWidth) {
    const px = parseInt(m[1], 10);
    if (px > 400 && !m[0].includes('max-width')) {
      warnings.push(`${relPath}: <img width="${m[1]}"> without max-width — may overflow mobile`);
    }
  }

  // 6. Arabic pages missing dir=rtl
  const isArabicPage = relPath.startsWith('ar/') || /<html[^>]+lang=["']ar["']/i.test(content);
  if (isArabicPage && !/<html[^>]+dir=["']rtl["']/i.test(content)) {
    failures.push(`${relPath}: Arabic page missing dir="rtl" on <html>`);
  }

  // 7. Generated article pages must have responsive CSS
  const isArticle = /insights\/[^/]+\.html$/.test(relPath);
  const isMarketOutlook = /market-outlook\/[^/]+\.html$/.test(relPath);
  if ((isArticle || isMarketOutlook) && !content.includes('responsive.css')) {
    warnings.push(`${relPath}: generated page missing responsive.css`);
  }

  // 8. Min-width in inline styles larger than viewport
  const minWidths = [...content.matchAll(/style="[^"]*min-width\s*:\s*(\d+)px[^"]*"/gi)];
  for (const m of minWidths) {
    if (parseInt(m[1], 10) > 1024) {
      warnings.push(`${relPath}: inline min-width:${m[1]}px may cause overflow on most screens`);
    }
  }
}

// ── CSS file checks ───────────────────────────────────────────────────────────

function checkCssFile(file) {
  const content = fs.readFileSync(file, 'utf8');
  const relPath = rel(file);

  // Look for min-width values > 100vw without matching overflow:auto/scroll
  const minWidths = [...content.matchAll(/min-width\s*:\s*(\d+)px/gi)];
  for (const m of minWidths) {
    const px = parseInt(m[1], 10);
    if (px > 1400) {
      warnings.push(`${relPath}: min-width:${m[1]}px — may cause horizontal overflow on typical screens`);
    }
  }
}

// ── Main ──────────────────────────────────────────────────────────────────────

const htmlFiles = collectHtmlFiles();
for (const file of htmlFiles) {
  try {
    checkPage(file);
  } catch (err) {
    warnings.push(`${rel(file)}: could not check — ${err.message}`);
  }
}

// Check CSS files
const cssFiles = [
  path.join(ROOT, 'styles.css'),
  path.join(ROOT, 'landing.css'),
  path.join(ROOT, 'css', 'market', 'market-portal.css'),
  path.join(ROOT, 'css', 'responsive.css')
];
for (const file of cssFiles) {
  if (fs.existsSync(file)) checkCssFile(file);
}

// ── Report ─────────────────────────────────────────────────────────────────────

console.log(`[responsive] Checked ${pagesChecked} pages, ${tablesChecked} tables`);

if (warnings.length) {
  warnings.forEach((w) => console.warn(`[responsive] WARN: ${w}`));
  console.log(`[responsive] ${warnings.length} warning(s) found`);
}

if (failures.length) {
  failures.forEach((f) => console.error(`[responsive] FAIL: ${f}`));
  process.exit(1);
}

console.log('[responsive] check:responsive passed.');
